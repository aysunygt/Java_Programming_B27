package day4_variables_cont;

public class MyName {
    /*
    declare your name as multiple char variables
    print your name, see what happens

     */
    public static void main(String[] args) {

        char first = 'A';
        char second = 'Y';
        char third = 'S';
        char fourth = 'U';
        char fifth = 'N';

        // char a = 'a', b = 'b', c = 'c';

        System.out.print(first);
        System.out.print(second);
        System.out.print(third);
        System.out.print(fourth);
        System.out.print(fifth);
        System.out.println();

        System.out.println("My name is " + first + second + third + fourth + fifth);// concatenate a String in the beginning, this will concatenate all the chars together
        System.out.println();
        System.out.println(first + second + third + fourth + fifth);






    }
}
