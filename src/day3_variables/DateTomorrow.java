package day3_variables;

public class DateTomorrow {
    /*
    declare and asign 3 variables (numbers)
    month , day, year

    print the date in this format:

        Date: month \ day \ year

        also update the dat for tomorrows date
            Tomorrow's Date: month \ day \ year


     */
    public static void main(String[] args) {

        int month, day, year; // made 3 variables,, al of them are int type
        month = 8;
        day = 26;
        year = 2022;

        System.out.println("Today's Date: " + month + "\\" + day + "\\" + year);

        month = 8;
        day = 27;
        year = 2022;

        System.out.println("Tomorrow's Date: " + month + "\\" + day + "\\" + year);







    }
}
