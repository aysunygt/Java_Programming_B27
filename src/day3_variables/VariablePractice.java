package day3_variables;

public class VariablePractice {
    public static void main(String[] args) {

        byte age; // declare a byte variable that is called age
        age = 10; // assigned 10 to the age variable
        System.out.println(age); // printing the age variable, which means I will print the value of the variable
        System.out.println("age"); // this will print the characters age, not the variable that has a number value
        System.out.println("20"); // this will print the characters 2 and 0
        System.out.println(20); // printing the int 20 -> by default 20 is int type
        System.out.println(age + 1); // age value is 10, and then it adds 1 , 10 + 1 and prints 11

        // byte num = 200; 200 is too big for byte types

        short num = 200; // declared and assigned at the same time






    }
}
