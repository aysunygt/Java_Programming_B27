package day8_scanner;

import java.util.Scanner;

public class AddNumbers {
    /*
    create a program that will as the user to enter two number. Add the numbers and print the result
     */
    public static void main(String[] args) {

        Scanner input = new Scanner(System.in); // Scanner object
        System.out.println("Enter number 1");
        int num1 = input.nextInt();
        System.out.println("Enter number 2");
        int num2 = input.nextInt();

        System.out.println(num1 + num2);// input.nextInt(); + input.nextInt();  --> we can do this too, not recommended. Just to show










    }
}
