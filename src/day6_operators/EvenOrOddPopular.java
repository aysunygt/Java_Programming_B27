package day6_operators;

public class EvenOrOddPopular {

    /*
    define a number
    find out if it is even or odd
     */
    public static void main(String[] args) {

        int num = 20;
        boolean isEven = num % 2 == 0;
        boolean isOdd = num % 2 != 0; // num % 2 == 1    -> isEven != false
        System.out.println("Is even: " + isEven);
        System.out.println("Is odd: " + isOdd);









    }
}
